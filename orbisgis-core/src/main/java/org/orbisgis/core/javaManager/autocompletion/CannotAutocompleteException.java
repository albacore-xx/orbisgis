package org.orbisgis.core.javaManager.autocompletion;

public class CannotAutocompleteException extends Exception {

	public CannotAutocompleteException() {
		super();
	}

	public CannotAutocompleteException(String message, Throwable cause) {
		super(message, cause);
	}

	public CannotAutocompleteException(String message) {
		super(message);
	}

	public CannotAutocompleteException(Throwable cause) {
		super(cause);
	}


}
