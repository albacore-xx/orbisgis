package org.orbisgis.core.geocognition.mapContext;

public class GeocognitionException extends Exception {

	public GeocognitionException() {
		super();
	}

	public GeocognitionException(String message, Throwable cause) {
		super(message, cause);
	}

	public GeocognitionException(String message) {
		super(message);
	}

	public GeocognitionException(Throwable cause) {
		super(cause);
	}

}
