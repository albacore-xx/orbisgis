package org.orbisgis.core.edition;

public class EditableElementException extends Exception {

	public EditableElementException() {
		super();
	}

	public EditableElementException(String message, Throwable cause) {
		super(message, cause);
	}

	public EditableElementException(String message) {
		super(message);
	}

	public EditableElementException(Throwable cause) {
		super(cause);
	}

}
