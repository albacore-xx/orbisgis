package org.gdms.sql.strategies;

import java.util.ArrayList;

import org.gdms.data.values.Value;
import org.gdms.driver.DriverException;
import org.gdms.driver.ObjectDriver;

/**
 * Decorator that maps rows to the decorated Driver. It can replicate rows,
 * filter rows and change the order of the rows
 *
 * @author Fernando Gonzalez Cortes
 */
public class RowMappedDriver extends AbstractMetadataSQLDriver implements
		ObjectDriver {

	private ArrayList<Integer> map;
	private ObjectDriver source;

	/**
	 * Creates a new instance.
	 *
	 * @throws DriverException
	 *
	 */
	public RowMappedDriver(ObjectDriver source,
			ArrayList<Integer> map) throws DriverException {
		super(source.getMetadata());
		this.source = source;
		this.map = map;
	}

	public Value getFieldValue(long rowIndex, int fieldId)
			throws DriverException {
		return source.getFieldValue(map.get((int) rowIndex), fieldId);
	}

	public long getRowCount() throws DriverException {
		return map.size();
	}
}