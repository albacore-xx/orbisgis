package org.gdms.sql.function.spatial.geometry.predicates;

import org.gdms.data.values.Value;
import org.gdms.data.values.ValueFactory;
import org.gdms.sql.function.FunctionException;

import com.vividsolutions.jts.geom.Geometry;

public class Relate extends AbstractSpatialPredicateFunction {

	public Value evaluateResult(final Value[] args) throws FunctionException {
		final Geometry geom1 = args[0].getAsGeometry();
		final Geometry geom2 = args[1].getAsGeometry();
		return ValueFactory.createValue(geom1.relate(geom2).toString());
	}

	public String getName() {
		return "Relate";
	}

	public boolean isAggregate() {
		return false;
	}

	public String getDescription() {
		return "Return a 9-character String representation of the 2 geometries IntersectionMatrix";
	}

	public String getSqlOrder() {
		return "select Relate(the_geom1,the_geom2) from myTable;";
	}

}